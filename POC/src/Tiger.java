public class Tiger extends Mammal {
    int tail;
    boolean meow;

    public int getTail() {
        return tail;
    }

    public void setTail(int tail) {
        this.tail = tail;
    }

    public boolean isMeow() {
        return meow;
    }

    public void setMeow(boolean meow) {
        this.meow = meow;
    }
}
